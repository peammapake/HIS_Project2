public class LabTest
{
    private String labTestName;

    private String result;

    public LabTest(String name, String result)
    {
        this.labTestName = name;
        this.result = result;
    }

    public String getLabTestName()
    {
        return labTestName;
    }

    public String getResult()
    {
        return result;
    }
}
